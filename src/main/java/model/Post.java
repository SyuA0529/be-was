package model;

public class Post {
    private String postId;
    private int userId;
    private String title;
    private String content;

    public String getPostId() {
        return postId;
    }

    public int getUserId() {
        return userId;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }
}
